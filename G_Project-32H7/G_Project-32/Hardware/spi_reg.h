/**
  ******************************************************************************
  * @file    spi_reg.h
  * @brief   SPI寄存器读写模块头文件
  * @details 本模块提供与外部芯片通信的SPI接口抽象层
  *          主要功能：
  *          1. 16位SPI通信协议实现
  *          2. 寄存器读写操作封装
  *          3. 批量数据传输支持
  *          4. 芯片选择控制
  *          
  *          应用场景：
  *          - ADC数据采集
  *          - 滤波器系数加载
  *          - 外部芯片配置
  *          - 状态寄存器读取
  *          
  *          技术特点：
  *          - 使用STM32硬件SPI1外设
  *          - 16位数据传输格式
  *          - 自动片选控制
  *          - 错误处理机制
  ******************************************************************************
  */

#ifndef __SPI_REG_H__
#define __SPI_REG_H__

/* Includes ------------------------------------------------------------------*/
#include "stm32h7xx_hal.h"  // STM32 HAL库
#include "spi.h"             // SPI驱动接口

/* Exported defines - 通信协议定义 --------------------------------------*/
/**
 * @brief  单次写操作命令码
 * @details WRITE_SIGLE = 0xAA01
 *          用于向外部芯片写入单个寄存器
 *          协议格式：[命令码][地址][数据]
 */
#define WRITE_SIGLE       0xaa01

/**
 * @brief  连续写操作命令码
 * @details WRITE_CURRENT = 0xAA02
 *          用于向外部芯片连续写入多个寄存器
 *          协议格式：[命令码][起始地址][数据1][数据2]...[数据N]
 */
#define WRITE_CURRENT     0xaa02

/**
 * @brief  单次读操作命令码
 * @details READ_SIGLE = 0xAA03
 *          用于从外部芯片读取单个寄存器
 *          协议格式：[命令码][地址] → [返回数据]
 */
#define READ_SIGLE        0xaa03

/**
 * @brief  连续读操作命令码
 * @details READ_CURRENT = 0xAA04
 *          用于从外部芯片连续读取多个寄存器
 *          协议格式：[命令码][起始地址] → [返回数据1][数据2]...[数据N]
 */
#define READ_CURRENT      0xaa04

/**
 * @brief  最后一帧标志
 * @details LAST_FRAME = 0xA5A5
 *          用于标识数据传输的最后一帧
 *          在批量传输时使用，便于接收方识别传输结束
 */
#define LAST_FRAME 0xa5a5

/* Exported defines - 硬件配置 ----------------------------------------*/
/**
 * @brief  SPI外设句柄
 * @details M_SPI = hspi1
 *          使用STM32的SPI1外设进行通信
 *          硬件配置：
 *          - 主机模式
 *          - 16位数据格式
 *          - 1.5Mbps波特率
 *          - CPOL=0, CPHA=0模式
 */
#define M_SPI hspi1

/**
 * @brief  SPI片选引脚控制宏
 * @details SPI_CS_L: 设置片选为低电平（选中芯片）
 *          SPI_CS_H: 设置片选为高电平（取消选中）
 *          连接到STM32的PG10引脚
 */
#define SPI_CS_L HAL_GPIO_WritePin(GPIOG,GPIO_PIN_10,GPIO_PIN_RESET)
#define SPI_CS_H HAL_GPIO_WritePin(GPIOG,GPIO_PIN_10,GPIO_PIN_SET)

/* Exported function prototypes ---------------------------------------------*/
/**
 * @brief  SPI寄存器写操作函数
 * @param  src: 源数据指针，指向要写入的数据数组
 * @param  addr: 目标寄存器起始地址
 * @param  size: 要写入的数据个数（16位字数）
 * @retval None
 * @details 通过SPI向外部芯片写入寄存器数据
 *          
 *          操作流程：
 *          1. 拉低片选信号（选中芯片）
 *          2. 发送写操作命令码
 *          3. 发送目标寄存器地址
 *          4. 连续发送指定数量的数据
 *          5. 拉高片选信号（取消选中）
 *          
 *          数据格式：
 *          - 16位数据格式
 *          - 大端序传输（MSB先行）
 *          - 支持连续地址写入
 *          
 *          应用示例：
 *          @code
 *          uint16_t data[] = {0x1234, 0x5678, 0x9ABC};
 *          spi_reg_write(data, 0x1000, 3);  // 写入3个数据到地址0x1000
 *          @endcode
 *          
 *          注意事项：
 *          - 确保目标地址有效
 *          - 检查数据数组大小
 *          - 注意字节序问题
 *          - 处理SPI传输错误
 */
void spi_reg_write(const uint16_t *src, uint16_t addr, uint16_t size);

/**
 * @brief  SPI寄存器读操作函数
 * @param  src: 目标数据指针，用于存储读取的数据
 * @param  addr: 源寄存器起始地址
 * @param  size: 要读取的数据个数（16位字数）
 * @retval None
 * @details 通过SPI从外部芯片读取寄存器数据
 *          
 *          操作流程：
 *          1. 拉低片选信号（选中芯片）
 *          2. 发送读操作命令码
 *          3. 发送源寄存器地址
 *          4. 连续读取指定数量的数据
 *          5. 拉高片选信号（取消选中）
 *          
 *          数据格式：
 *          - 16位数据格式
 *          - 大端序传输（MSB先行）
 *          - 支持连续地址读取
 *          
 *          应用示例：
 *          @code
 *          uint16_t buffer[10];
 *          spi_reg_read(buffer, 0x2000, 10);  // 从地址0x2000读取10个数据
 *          @endcode
 *          
 *          注意事项：
 *          - 确保源地址有效
 *          - 检查目标缓冲区大小
 *          - 注意字节序问题
 *          - 处理SPI传输错误
 *          - 考虑读取延迟
 */
void spi_reg_read(uint16_t *src, uint16_t addr, uint16_t size);

/* ============================================================================
 * 使用说明和协议详解
 * ============================================================================
 * 
 * @brief  SPI通信协议说明
 * @details 
 * 本模块使用自定义的SPI通信协议，支持寄存器读写操作：
 * 
 * 写操作协议：
 * ┌─────────┬─────────┬─────────┬─────────┬─────────┐
 * │ 命令码  │  地址   │  数据1  │  数据2  │   ...   │
 * │ 16位    │ 16位    │ 16位    │ 16位    │ 16位    │
 * └─────────┴─────────┴─────────┴─────────┴─────────┘
 * 
 * 读操作协议：
 * ┌─────────┬─────────┬─────────────────────────────────────┐
 * │ 命令码  │  地址   │           返回数据                  │
 * │ 16位    │ 16位    │    N×16位                           │
 * └─────────┴─────────┴─────────────────────────────────────┘
 * 
 * @brief  命令码详解
 * @details 
 * - 0xAA01: 单次写操作，写入一个寄存器
 * - 0xAA02: 连续写操作，写入多个连续寄存器
 * - 0xAA03: 单次读操作，读取一个寄存器
 * - 0xAA04: 连续读操作，读取多个连续寄存器
 * 
 * @brief  时序要求
 * @details 
 * - CS低电平有效，传输期间保持低电平
 * - 数据在SCLK上升沿采样
 * - 16位数据传输，MSB先行
 * - 字节间无延迟，连续传输
 * 
 * @brief  错误处理
 * @details 
 * - SPI传输超时检测
 * - 片选信号冲突检查
 * - 数据完整性验证
 * - 硬件故障恢复
 * 
 * @brief  性能优化
 * @details 
 * - 使用DMA传输（大数据量）
 * - 中断驱动模式
 * - 双缓冲机制
 * - 硬件CRC校验
 */

#endif /* __SPI_REG_H__ */
