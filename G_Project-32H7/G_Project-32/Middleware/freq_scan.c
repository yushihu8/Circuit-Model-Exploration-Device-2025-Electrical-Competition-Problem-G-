/**
  ******************************************************************************
  * @file    freq_scan.c
  * @brief   频率扫描模块实现
  * @details 本文件实现了自动频率扫描功能，用于获取未知系统的频率响应特性
  *          
  *          主要功能：
  *          1. 控制AD9954 DDS芯片生成扫频信号
  *          2. 通过SPI读取ADC采集的输入输出信号
  *          3. 使用FFT分析信号的频域特性
  *          4. 计算各频率点的传递函数H(jω)
  *          5. 输出频率响应数据用于后续滤波器设计
  *          
  *          扫频参数：
  *          - 频率范围：200Hz - 50kHz
  *          - 频率步进：200Hz
  *          - 扫频点数：250个
  *          - 采样长度：1024点FFT
  ******************************************************************************
  */

#include "freq_scan.h"

/**
  * @brief  增益数组 - 存储各频率点的幅度响应
  * @details gain[i] = |H(jω)|? = Re? + Im?
  *          用于调试输出和频率响应分析
  */
float32_t gain[SAMPLE_NUM];

/**
  * @brief  频率扫描主函数
  * @param  sample_buff: 输出参数，存储各频率点的传递函数H(jω)
  * @retval None
  * @details 实现完整的频率扫描流程：
  *          1. 初始化DDS输出为200Hz，最大幅度
  *          2. 循环扫描250个频率点（200Hz-50kHz）
  *          3. 对每个频率点：
  *             - 启动ADC采集
  *             - 读取输入信号并FFT分析
  *             - 读取输出信号并FFT分析
  *             - 计算传递函数H(jω) = H_out/H_in
  *             - 更新DDS到下一个频率
  *          4. 输出所有频率点的增益值
  *          5. 关闭DDS输出
  *          
  *          信号流程：
  *          DDS信号 → 被测系统 → ADC采集 → FFT分析 → 传递函数计算
  *          
  *          注意事项：
  *          - 每个频率点需要20ms稳定时间
  *          - 使用基频分量（i+1）进行计算，避免直流分量干扰
  *          - 复数除法处理相位信息
  */
void freq_scan(complex *sample_buff)
{
  complex h_in, h_out;  // 输入输出信号的复数FFT结果
  
  // ========== 初始化扫频参数 ==========
  // 设置DDS初始频率为200Hz，幅度为最大值（16383对应满量程）
  AD9954_SetFre(200);           // 设置DDS输出频率为200Hz（起始频率）
  AD9954_Voltage_Control(16383); // 设置DDS输出幅度为最大值（14位DAC满量程）
  
  HAL_Delay(20);  // 等待DDS输出稳定
  
  // ========== 频率扫描主循环 ==========
  // 扫描250个频率点：200Hz, 400Hz, 600Hz, ..., 50kHz
  for(uint16_t i = 0; i < SAMPLE_NUM; i++)
  {
    // ========== 启动ADC采集 ==========
    uint16_t cmd = 1;  // ADC启动命令
    
    HAL_Delay(20);  // 等待频率稳定
    spi_reg_write(&cmd, 0xbb01, 1);  // 写入ADC启动命令到寄存器0xbb01
    HAL_Delay(20);  // 等待ADC采集完成
    spi_reg_read((uint16_t *)adc_buff, 0, 2 * FFT_LENGTH);  // 读取ADC数据（1024点×2通道）
    
    // ========== 输入信号FFT分析 ==========
    fft_calc_a();  // 对输入信号进行FFT变换，结果存储在fft_in数组中
    // 提取基频分量（i+1）的复数值
    // fft_in数组格式：[实部0, 虚部0, 实部1, 虚部1, ..., 实部511, 虚部511]
    h_in.r = fft_in[2 * (i + 1)];      // 输入信号基频分量实部
    h_in.i = fft_in[2 * (i + 1) + 1];  // 输入信号基频分量虚部
   
    // ========== 输出信号FFT分析 ==========
    fft_calc_b();  // 对输出信号进行FFT变换，结果存储在fft_in数组中
    // 提取基频分量（i+1）的复数值
    h_out.r = fft_in[2 * (i + 1)];      // 输出信号基频分量实部
    h_out.i = fft_in[2 * (i + 1) + 1];  // 输出信号基频分量虚部
    
    // ========== 计算传递函数 ==========
    // H(jω) = H_out(jω) / H_in(jω)
    // 使用复数除法保持幅度和相位信息
    sample_buff[i] = complex_div(&h_out, &h_in);
    
    // ========== 更新DDS频率 ==========
    // 设置下一个频率点：200Hz * (i + 2)
    // i=0: 400Hz, i=1: 600Hz, ..., i=249: 50200Hz
    AD9954_SetFre(200 * (i + 2));
    
    // ========== 计算并存储增益值 ==========
    // |H(jω)|? = Re? + Im?
    // 用于调试输出和频率响应分析
    gain[i] = sample_buff[i].r * sample_buff[i].r + sample_buff[i].i * sample_buff[i].i;
  }
  
  // ========== 输出频率响应数据 ==========
  // 打印所有频率点的增益值，用于调试和验证
  for(uint16_t k = 0; k < SAMPLE_NUM; k++)
  {
    printf("%f\n", gain[k]);  // 输出每个频率点的幅度平方
  }
  printf("END\n");  // 扫描完成标志
  
  // ========== 关闭DDS输出 ==========
  AD9954_SetFre(0);           // 设置DDS频率为0Hz（停止输出）
  AD9954_Voltage_Control(0);  // 设置DDS幅度为0（完全关闭）
}
