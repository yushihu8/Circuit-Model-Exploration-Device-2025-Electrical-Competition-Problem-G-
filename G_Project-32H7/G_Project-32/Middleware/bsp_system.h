/**
  ******************************************************************************
  * @file    bsp_system.h
  * @brief   板级支持包系统头文件
  * @details 本文件是整个项目的核心头文件，统一包含所有硬件驱动和功能模块
  *          
  *          设计理念：
  *          1. 单一包含点：通过包含此文件即可访问所有系统功能
  *          2. 模块化管理：按功能模块组织头文件包含
  *          3. 依赖管理：处理模块间的依赖关系
  *          4. 便于维护：修改包含关系只需在此文件中进行
  *          
  *          系统架构：
  *          - 基础库层：标准C库、数学库
  *          - 硬件抽象层：STM32 HAL库、SPI驱动
  *          - 硬件驱动层：AD9954 DDS、FFT处理
  *          - 应用功能层：滤波器设计、频率扫描
  ******************************************************************************
  */

#ifndef __BSP_SYSTEM_H__
#define __BSP_SYSTEM_H__

/* ============================================================================
 * 基础库包含
 * ============================================================================ */
/**
 * @brief 标准输入输出库
 * @details 提供printf、scanf等标准I/O函数
 *          主要用于调试输出和系统信息打印
 */
#include "stdio.h"

/**
 * @brief 数学库
 * @details 提供数学函数如sin、cos、sqrt、pow等
 *          用于滤波器计算、频率分析等数学运算
 */
#include "math.h"

/* ============================================================================
 * 硬件抽象层包含
 * ============================================================================ */
/**
 * @brief SPI驱动接口
 * @details STM32 HAL库的SPI驱动
 *          用于与外部芯片（AD9954、ADC、滤波器芯片）通信
 */
#include "spi.h"

/* ============================================================================
 * 硬件驱动层包含
 * ============================================================================ */
/**
 * @brief AD9954 DDS芯片驱动
 * @details 直接数字合成器控制接口
 *          功能：
 *          - 频率合成（200Hz-400MHz）
 *          - 幅度控制（14位精度）
 *          - 相位控制
 *          - 线性扫频模式
 */
#include "ad9954.h"

/**
 * @brief FFT快速傅里叶变换处理
 * @details 频域分析核心模块
 *          功能：
 *          - 1024点FFT/IFFT变换
 *          - 复数数组处理
 *          - 频谱分析
 *          - 基频分量提取
 */
#include "fft.h"

/* ============================================================================
 * 通信接口层包含
 * ============================================================================ */
/**
 * @brief SPI寄存器读写接口
 * @details 外部芯片寄存器访问抽象层
 *          功能：
 *          - 16位SPI通信协议
 *          - 寄存器读写操作
 *          - 批量数据传输
 *          - 芯片选择控制
 */
#include "spi_reg.h"

/* ============================================================================
 * 应用功能层包含
 * ============================================================================ */
/**
 * @brief 滤波器计算模块
 * @details 自适应滤波器设计核心算法
 *          功能：
 *          - 最小二乘法拟合
 *          - 矩阵方程求解
 *          - 双线性变换
 *          - Q31量化处理
 *          - 滤波器类型识别
 */
#include "h_calc.h"

/**
 * @brief 频率扫描模块
 * @details 系统频率响应测量
 *          功能：
 *          - 自动扫频控制
 *          - 传递函数测量
 *          - 频域特性分析
 *          - 增益响应计算
 */
#include "freq_scan.h"

/* ============================================================================
 * 使用说明
 * ============================================================================ */
/**
 * @brief  使用方法
 * @details 在应用文件中只需包含此头文件即可访问所有系统功能：
 *          @code
 *          #include "bsp_system.h"
 *          
 *          int main(void)
 *          {
 *              // 系统初始化
 *              AD9954_Init();
 *              
 *              // 频率扫描
 *              complex response[SAMPLE_NUM];
 *              freq_scan(response);
 *              
 *              // 滤波器设计
 *              coef_calc(response);
 *              analog_coef ac = matrix_calc();
 *              digital_coef dc = bilinear_transform_quant(&ac);
 *              
 *              return 0;
 *          }
 *          @endcode
 *          
 * @brief  模块依赖关系
 * @details 
 *          freq_scan.h → h_calc.h → spi_reg.h → spi.h
 *                        ↓              ↓
 *                    fft.h         ad9954.h
 *                        ↓              ↓
 *                    math.h       stdio.h
 *                        ↓              ↓
 *                stm32h7xx_hal.h
 */

#endif /* __BSP_SYSTEM_H__ */
